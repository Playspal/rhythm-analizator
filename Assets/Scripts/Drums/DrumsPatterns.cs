        public string A(DrumsLoopBar bar)
        {
            var s = string.Empty;
            var sOriginal = string.Empty;
            var sMask = string.Empty;
            
            var beats = 0;

            for (var i = 0; i < bar.Length; i++)
            {
                if (bar.BeatsKick[i].Type == DrumsBeatType.Kick)
                {
                    s += "K";
                    sOriginal += "K";
                    sMask += "K";
                    beats++;
                }
                else if (bar.BeatsSnare[i].Type == DrumsBeatType.Snare)
                {
                    s += "S";
                    sOriginal += "S";
                    sMask += "S";
                    beats++;
                }
                else
                {
                    s += "x";
                    sOriginal += "x";
                }
            }

            if (s.Length != 16)
            {
                return s;
            }

            var result = string.Empty;
            var resultMatch = int.MinValue;

            
            foreach (var pattern in _pattern)
            {
                var patternMask = string.Empty;

                for (var i = 0; i < pattern.Length; i++)
                {
                    var charA = pattern.Substring(i, 1);

                    if (charA == "K")
                    {
                        patternMask += "K";
                    }
                    
                    if (charA == "S")
                    {
                        patternMask += "S";
                    }
                }

                if (patternMask != sMask)
                {
                    continue;
                }

                var match = 0;
                
                for (var i = 0; i < pattern.Length; i++)
                {
                    var charA = pattern.Substring(i, 1);
                    var charALeft = pattern.Substring(i > 0 ? i - 1 : i, 1);
                    var charALeftLeft = pattern.Substring(i > 1 ? i - 2 : i, 1);
                    var charARight = pattern.Substring(i < pattern.Length - 1 ? i + 1 : i, 1);
                    var charARightRight = pattern.Substring(i < pattern.Length - 2 ? i + 2 : i, 1);
                    
                    
                    var charB = s.Substring(i, 1);
                    var charBLeft = s.Substring(i > 0 ? i - 1 : i, 1);
                    var charBLeftLeft = s.Substring(i > 1 ? i - 2 : i, 1);
                    var charBRight = s.Substring(i < s.Length - 1 ? i + 1 : i, 1);
                    var charBRightRight = s.Substring(i < s.Length - 2 ? i + 2 : i, 1);

                    if (charA == "x" && charB != "x")
                    {
                        if (charALeft != "x")
                        {
                            match += 2;
                        }
                        else if (charARight != "x")
                        {
                            match += 2;
                        }
                        else if (charALeftLeft != "x")
                        {
                            match += 1;
                        }
                        else if (charARightRight != "x")
                        {
                            match += 1;
                        }
                        else
                        {
                            match -= 1;
                        }
                    }
                    else if (charA != "x" && charB == "x")
                    {
                        if (charBLeft != "x")
                        {
                            match += 2;
                        }
                        else if (charBRight != "x")
                        {
                            match += 2;
                        }
                        else if (charBLeftLeft != "x")
                        {
                            match += 1;
                        }
                        else if (charBRightRight != "x")
                        {
                            match += 1;
                        }
                        else
                        {
                            match -= 1;
                        }
                    }
                    else if (charA != "x" && charB != "x")
                    {
                        match += i % 4 == 0 ? 8 : 4;
                    }
                    else
                    {
                        match += 1;
                    }
                }

//                Debug.Log(s + " >> " + pattern + " >> " + match);
                
                if (match >= resultMatch)
                {
                    resultMatch = match;
                    result = pattern;
                }
            }

            Debug.Log(s + " >> " + result + " >> " + resultMatch);

            if (string.IsNullOrEmpty(result))
            {
                return sOriginal;
            }
            
            return result;
        }

